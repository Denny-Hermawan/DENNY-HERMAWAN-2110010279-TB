    private void binputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_binputActionPerformed
        // TODO add your handling code here
        try {
            // Membuat query SQL untuk menambahkan data ke dalam tabel tb_datapegawai berdasarkan nilai dari komponen-komponen UI
            String sql = "INSERT INTO tb_datapegawai (id_pegawai, NIP, nama, alamat, jenis_kelamin, jabatan, no_hp) VALUES (?, ?, ?, ?, ?, ?, ?)";

            // Mendapatkan koneksi ke database
            Connection conn = koneksi.getConnection();

            // Membuat objek PreparedStatement untuk menjalankan query SQL
            PreparedStatement pst = conn.prepareStatement(sql);

            // Mengambil nilai dari komponen-komponen UI dan menetapkannya ke dalam parameter query
            pst.setString(1, tfidpegawai.getText());
            pst.setString(2, tfnip.getText());
            pst.setString(3, tfnama.getText());
            pst.setString(4, tfalamat.getText());
            pst.setString(5, (String) cbjeniskelamin.getSelectedItem());
            pst.setString(6, tfjabatan.getText());
            pst.setString(7, tfnohp.getText());

            // Menjalankan query untuk menyimpan data ke dalam tabel tb_datapegawai
            int rowsInserted = pst.executeUpdate();

            // Menampilkan pesan sukses jika penyimpanan data berhasil
            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
                this.setVisible(false);

                // Menampilkan kembali frame FDataPegawai
                FDataPegawai frameDataPegawai = new FDataPegawai();
                frameDataPegawai.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Gagal menyimpan data");
            }

            // Bersihkan field di FDataPegawai setelah disimpan
            tfidpegawai.setText("");
            tfnip.setText("");
            tfnama.setText("");
            tfalamat.setText("");
            cbjeniskelamin.setSelectedIndex(0);
            tfjabatan.setText("");
            tfnohp.setText("");

        } catch (SQLException e) {
            // Menampilkan pesan kesalahan jika terjadi SQLException
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_binputActionPerformed

